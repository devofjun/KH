package sec04.exam01;

public class Calculator {
	//전원켜기
	void powerOn() {
		System.out.println("전원을 켭니다.");
	}
	//전원끄기
	void powerOff() {
		System.out.println("전원을 끕니다.");
	}
	//더하기
	int plus(int x, int y) {
		return x + y;
	}
	//나누기
	double divide(int x, int y) {
		return (double)x / y;
	}
	
	
	
}
